
public class Data {

    String ulica;
    String numer;

    public Data(String ulica, String numer) {
        this.ulica = ulica;
        this.numer = numer;
    }

    public String getUlica() {
        return ulica;
    }

    public void setUlica(String ulica) {
        this.ulica = ulica;
    }

    public String getNumer() {
        return numer;
    }

    public void setNumer(String numer) {
        this.numer = numer;
    }

}
